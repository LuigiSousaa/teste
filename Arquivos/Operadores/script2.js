// Operadores de Atribuição

var numero = 5;
// atribuição +=
numero += 2;
document.write("<br/>O Resultado 5 += 2 é ", numero);
// atribuiçãi -=
numero = 5; // atribuição novamente 5  variavel número
numero -= 2;
document.write("<br/>O Resultado 5 -= 2 é ", numero);
// atribuição *=
numero = 5; // atribuição novamente 5  variavel número
numero *= 2;
document.write("<br/>O Resultado 5 *= 2 é ", numero);
// atribuição /=
numero = 5; // atribuição novamente 5  variavel número
numero /= 2;
document.write("<br/>O Resultado 5 /= 2 é ", numero);
// atribuição %=
numero = 5; // atribuição novamente 5  variavel número
numero %= 2;
document.write("<br/>O Resultado 5 %= 2 é ", numero);



