// Operadores Aritméticos

var numero;
//adição
numero = parseInt(5+2);
document.write("O Resultado da Adição de 5+2 é ", numero);
//subtração
numero = parseInt(5-2);
document.write("<br/>O Resultado da Subtração de 5-2 é ", numero);
//Multiplicação
numero = parseInt(5*2);
document.write("<br/>O Resultado da Multiplicação de 5*2 é ", numero);
//Divisão
numero = parseFloat(5/2);
document.write("<br/>O Resultado da Divisão de 5/2 é ", numero);
//Resto
numero = parseInt(5%2);
document.write("<br/>O Resultado do Resto de 5%2 é ", numero);